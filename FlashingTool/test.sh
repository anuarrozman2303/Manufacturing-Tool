# Install Python 3 and pip
sudo apt install -y python3 python3-pip

# Install Tkinter for Python 3
sudo apt install -y python3-tk

# Install esptool
pip3 install esptool

# Install Git
sudo apt install -y git

# Install MariaDB server
sudo apt install -y mariadb-server

# Install default libmysqlclient development package
sudo apt install -y libmysqlclient-dev

# Install Python 3 RPi.GPIO
sudo apt install -y python3-rpi.gpio

# Install libhidapi-hidraw0 and libhidapi development package
sudo apt install -y libhidapi-hidraw0 libhidapi-dev

# Install Python 3 development package
sudo apt install -y python3-dev

# Install GCC for AArch64 Linux
sudo apt install -y gcc-aarch64-linux-gnu
